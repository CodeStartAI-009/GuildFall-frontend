/* =================================
   DETAILS COMPONENT
================================= */

export default function Details({
  label,
  position,
  board = [],
  players = [],
  user
}) {

  if (!label) return null;

  const data = detailsData[label];
  if (!data) return null;

  /* ===============================
     GET TILE FROM BOARD
  =============================== */

  const tile = board[position];

  let ownerName = null;
  let isMine = false;

  if (tile && tile.owner) {

    const player = players.find(p => p.userId === tile.owner);

    ownerName = player?.username || "Unknown Player";

    isMine = user && tile.owner === user._id;
  }

  return (

    <div className="details-box">

      <h2>{label}</h2>

      <h4>{data.region}</h4>

      {/* ===============================
         OWNER INFO
      =============================== */}

      {ownerName && (

        <div className="details-owner">

          {isMine ? (
            <span style={{ color: "#4CAF50" }}>
              Owned by You
            </span>
          ) : (
            <>
              Owned by: <span>{ownerName}</span>
            </>
          )}

        </div>

      )}

      {/* ===============================
         TABLE
      =============================== */}

      <table className="details-table">

        <thead>
          <tr>
            <th>Level</th>
            <th>Name</th>
            <th>Mana</th>
          </tr>
        </thead>

        <tbody>

          {data.levels.map((lvl, index) => (
            <tr key={index}>
              <td>{lvl.level}</td>
              <td>{lvl.name}</td>
              <td>{lvl.cost}</td>
            </tr>
          ))}

        </tbody>

      </table>

    </div>

  );

}